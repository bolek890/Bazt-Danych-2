     private void displayStatistics() {
        try {
        
         Class.forName(boss_data_connection.getClassName());
         Connection con = DriverManager.getConnection(boss_data_connection.getURL(), boss_data_connection.getUser(), boss_data_connection.getPassword());
         PreparedStatement pst = con.prepareStatement("SELECT COUNT(status) AS licznik FROM zamowienie WHERE status NOT LIKE 'Zakończony'");
         ResultSet rs=pst.executeQuery();
         
        int count=0;
        while(rs.next()){
        count= rs.getInt("licznik");
        }
       
        orderCount.setText(String.valueOf(count));
        
         pst = con.prepareStatement("SELECT COUNT(id) AS licznik2 FROM magazyn");
         rs=pst.executeQuery();
         
        int count2=0;
        while(rs.next()){
        count2= rs.getInt("licznik2");
        }
       
        magazynyField.setText(String.valueOf(count2));
        
        
         pst = con.prepareStatement("SELECT COUNT(id) AS licznik3 FROM pracownik");
         rs=pst.executeQuery();
         
        int count3=0;
        while(rs.next()){
        count3= rs.getInt("licznik3");
        }
       
        pracownikField.setText(String.valueOf(count3));
        
        
        pst = con.prepareStatement("SELECT COUNT(id) AS licznik4 FROM produkt");
         rs=pst.executeQuery();
         
        int count4=0;
        while(rs.next()){
        count4= rs.getInt("licznik4");
        }
       
        artykulyField.setText(String.valueOf(count4));
        
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Błąd!");

        }
    }
