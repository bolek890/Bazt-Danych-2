    public void show_worker(){
    ArrayList<boss_worker> list = workersList();
    DefaultTableModel model= (DefaultTableModel)workerTable.getModel();
    model.setRowCount(0);
    Object[] row= new Object[5];
    for(int i=0; i<list.size(); i++){
        row[0]= list.get(i).getId();
        row[1]= list.get(i).getUzytkownik_id();
        row[2]= list.get(i).getImie();
        row[3]= list.get(i).getNazwisko();
        row[4]= list.get(i).getTyp_konta();
        model.addRow(row);
        }
    list.clear();
    }
    
