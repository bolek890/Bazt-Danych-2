    public ArrayList<boss_worker> workersList(){
        ArrayList<boss_worker> workerList = new ArrayList<>();
         try{
             Class.forName(boss_data_connection.getClassName());
             Connection con = DriverManager.getConnection(boss_data_connection.getURL(), boss_data_connection.getUser(), boss_data_connection.getPassword());
             String query1="Select * from dane_pracownikow_view";
             
             Statement st= con.createStatement();
             ResultSet rs= st.executeQuery(query1);
             boss_worker worker;
             while(rs.next()){
                 worker= new boss_worker(rs.getInt("id"), rs.getInt("uzytkownik_id"), rs.getString("imie"), rs.getString("nazwisko"), rs.getString("typ_konta"));
                 workerList.add(worker);
                }
             } 
         catch(Exception e){
                        System.out.println(e.getMessage()); 
          }
         return workerList;
    }
    
